# Определение комплексных чисел
z1 = complex(2, 3)  
z2 = complex(1, -4) 

# Сложение комплексных чисел
sum_z = z1 + z2

# Вычитание комплексных чисел
diff_z = z1 - z2

# Умножение комплексных чисел
prod_z = z1 * z2

# Деление комплексных чисел
div_z = z1 / z2

# Вывод результатов
print(f"Сумма: {sum_z}")
print(f"Разность: {diff_z}")
print(f"Произведение: {prod_z}")
print(f"Частное: {div_z}")

import cmath

# Определение комплексного числа
z3 = complex(-1, 1)

# Четвертая степень комплексного числа
z3_pow4 = z3**4

# Корень третьей степени комплексного числа
z3_root3 = cmath.exp(cmath.log(z3) / 3)  

# Вывод результатов
print(f"Четвертая степень: {z3_pow4}")
print(f"Корень третьей степени: {z3_root3}")
